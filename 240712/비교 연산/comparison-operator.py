a, b = map(int, input().split())

# a가 b보다 같거나 큰지 여부
if a >= b:
    print(1)
else:
    print(0)

# a가 b보다 큰지 여부
if a > b:
    print(1)
else:
    print(0)

# b가 a보다 같거나 큰지 여부
if b >= a:
    print(1)
else:
    print(0)

# b가 a보다 큰지 여부
if b > a:
    print(1)
else:
    print(0)

# a와 b가 같은지 여부
if a == b:
    print(1)
else:
    print(0)

# a와 b가 다른지 여부
if a != b:
    print(1)
else:
    print(0)